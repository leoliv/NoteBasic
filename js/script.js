let t = document.getElementById("caixa");

function negrito() {
    t.style.fontWeight = "bold";
}

function italico() {
    t.style.fontStyle = "italic";
}

function sublinhado() {
    t.style.textDecoration = "underline";
}

function maiusculo() {
    t.style.textTransform = "uppercase";
}

function menusculo() {
    t.style.textTransform = "lowercase";
}